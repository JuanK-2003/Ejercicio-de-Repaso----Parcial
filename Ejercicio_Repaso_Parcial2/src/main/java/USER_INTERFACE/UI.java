    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        if(!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() && 
                !jTextField3.getText().isEmpty() && !jTextField4.getText().isEmpty() 
                && !edit){
            pr.add(c, new Producto(jTextField1.getText(),
            jTextField2.getText(),Double.parseDouble(jTextField3.getText()),
            jTextField4.getText()));
            c++;
        }
        else if(edit){
          pr.get(in).setCodigo(jTextField1.getText());
          pr.get(in).setNombre(jTextField2.getText());
          pr.get(in).setPrecio(Double.parseDouble(jTextField3.getText()));
          pr.get(in).setExistencia(jTextField4.getText());
          edit = false;
          jButton1.setText("Agregar");
        }
        this.datosTabla();
        this.setTabla();
    }//GEN-LAST:event_jButton1MouseClicked

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        if(jTable1.getSelectedRow() >= 0){
            jButton1.setText("Editar");
            edit = true;
            in = jTable1.getSelectedRow();
            jTextField1.setText(pr.get(in).getCodigo());
            jTextField2.setText(pr.get(in).getNombre());
            jTextField3.setText(Double.toString(pr.get(in).getPrecio()));
            jTextField4.setText(pr.get(in).getExistencia());
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

